// ── HELPER ────────────────────────────────────────────────────────────────
// Look up an English string and return its translation in `lang`. Falls back
// to the English key itself if no translation exists for that language.
//
// Usage: t("back", parentLang)  →  "atrás" (Spanish) | "back" (English/missing)
//
// String interpolation is handled at the call site — translate templates
// (e.g. "Day {n}") into per-language templates and interpolate after lookup.
export function tLookup(englishKey, lang) {
  if (!englishKey || lang === "English" || !lang) return englishKey;
  const entry = UI_STRINGS[englishKey];
  if (!entry) return englishKey;
  const translated = entry[lang];
  if (translated == null) return englishKey;
  return translated;
}

// Translation coverage stats — useful for the "How translated is my UI?"
// signal we may show parents in the future.
export function translationCoverageFor(lang) {
  if (lang === "English") return 1;
  let covered = 0;
  let total = 0;
  for (const key of Object.keys(UI_STRINGS)) {
    total++;
    const entry = UI_STRINGS[key];
    if (entry && entry[lang] != null) covered++;
  }
  return total > 0 ? covered / total : 0;
}
